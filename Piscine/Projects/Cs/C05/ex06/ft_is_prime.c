#include <stdio.h>

int	ft_is_prime(int nb)
{
	int		x;

	if (nb <= 1)
		return (0);
	x = 2;
	while (nb % x != 0)
	{
		x++;
	}
	if (x == nb)
		return (1);
	return (0);
}
